function rollupOutcomes(
  outcomes: PipelineExecutionOutcome[],
  channel: ChannelType,
  engagementMetricLabel: string
): ChannelExecutionOutcomeRollup {
  const rows = outcomes.filter((o) => o.channel === channel);
  const total = rows.length;
  const deliveredCount = rows.filter((r) => r.delivered).length;
  const engagedCount = rows.filter((r) => r.delivered && r.engaged).length;
  const deliveryRatePct = total ? Math.round((deliveredCount / total) * 1000) / 10 : 0;
  const engagementAmongDeliveredPct = deliveredCount
    ? Math.round((engagedCount / deliveredCount) * 1000) / 10
    : 0;
  return {
    channel,
    total,
    deliveredCount,
    engagedCount,
    deliveryRatePct,
    engagementAmongDeliveredPct,
    engagementMetricLabel,
  };
}

